package chap04_Operators;

 class Arithmetic {
  static void main(String[] args) {
   int a = 5 ;
   int b = 4;
   System.out.println(a+b);
   System.out.println(a-b);
   System.out.println(a*b);
   System.out.println(a/b);
   System.out.println(a%b);


   double x = 8;
   double y = 3;
   System.out.println(x+y);
   System.out.println(x-y);
   System.out.println(x*y);
   System.out.println(x/y);
   System.out.println(x%y);


  }
}
