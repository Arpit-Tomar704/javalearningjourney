package chap03_VariablesandDataTypes;

 class Topic02_EscapeSequences {
    static void main(String[] args) {
        System.out.println("welcome to\tkgcoding");
        System.out.println("welcome to\bkgcoding");
        System.out.println("welcome to\nkgcoding");
        System.out.println("welcome to\'kgcoding\'");
        System.out.println("welcome to \"kgcoding\"");
        System.out.println("welcome to \\kgcoding\\");

    }
}
