package Challenges;

import java.util.Scanner;

public class chap06_Swap {
    static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        System.out.println("swaping Station ");
        System.out.print("Enter the  Value of first number a :");
        int a = input.nextInt();
        System.out.print("Enter the  value second number b :");
        int b = input.nextInt();
        int c = a;
        a = b;
        b = c;
        System.out.println("Swaping done  ");
        System.out.println("The Value a  is " +a);
        System.out.println("the of b is " +b);









    }
}
